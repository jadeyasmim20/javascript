// FOR somar de 1 até 1000:
//for: significa PARA, tem que ter um contador, condição, e incrementandouso, 'i' por boa pratica, porem é igual a count, i de identificador ou variavel de controle.
soma = 0;

for (i = 0; i <= 1000; i++) {
  soma += i;
  document.write(soma, ' ');
}
// soma é cumulativa.
//3: for( ;condição de parada; ) não necessariamente precisa ter todas as coisas mas deve-se usar ponto e virgula.
